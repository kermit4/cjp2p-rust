// when this gets to millions of peers, consider keeping less info about the slower ones
#[serde_as]
#[derive(Clone, Serialize, Deserialize, Debug)]
struct PeerInfo {
    delay: Duration,
    anti_ip_spoofing_token_they_expect: Value,
    anti_ip_spoofing_token_i_expect: u32, // to save memory, these could be generated by hashing
    // their host:port with a random unique to this node
    #[serde_as(as = "Base64")]
    public_key: Vec<u8>,
}
impl PeerInfo {
    fn new() -> Self {
        return Self {
            delay: Duration::from_millis(200),
            anti_ip_spoofing_token_they_expect: json!({}),
            anti_ip_spoofing_token_i_expect: rand::thread_rng().gen(),
            public_key: vec![],
        };
    }
}
struct PeerState {
    peer_map: HashMap<SocketAddr, PeerInfo>,
    peer_vec: Vec<SocketAddr>,
    socket: UdpSocket,
    boot: Instant,
    keypair: Keypair,
}
impl PeerState {
    fn new() -> Self {
        return Self {
            peer_map: HashMap::new(),
            peer_vec: Vec::new(),
            socket: UdpSocket::bind("0.0.0.0:24254").unwrap(),
            boot: Instant::now(),
            keypair: Builder::new("Noise_XK_25519_ChaChaPoly_SHA256".parse().unwrap())
                .generate_keypair()
                .unwrap(),
        };
    }
    fn save_key(&mut self, src: SocketAddr, cookie: Value) {
        debug!("saving token {cookie} for {src}");
        self.peer_map
            .get_mut(&src)
            .unwrap()
            .anti_ip_spoofing_token_they_expect = cookie;
        debug!("saved token verified {:?}", self.always_returned(src));
    }
    fn check_key(&self, messages: &Vec<Value>, src: SocketAddr) -> bool {
        for message_in in messages {
            if let Some(m) = message_in.get("AlwaysReturned") {
                if let Ok(t) = serde_json::from_value::<u32>(m.clone()) {
                    let correct_key = self.peer_map[&src].anti_ip_spoofing_token_i_expect;
                    return correct_key != t;
                }
            }
        }
        return true;
    }

    fn always_returned(&self, sa: SocketAddr) -> Vec<Value> {
        debug!("always_returned for {sa}");
        match self.peer_map.get(&sa) {
            None => return vec![],
            Some(p) => {
                let token = p.anti_ip_spoofing_token_they_expect.to_owned();
                if token == json!({}) {
                    return vec![];
                }
                debug!("always_returned for {sa} found {token}");
                return vec![serde_json::json!({"AlwaysReturned": token }),
                ];
            }
        }
    }

    fn probe_interfaces(&mut self) -> () {
        let to_probe: &mut HashSet<SocketAddr> = &mut HashSet::new();
        let addrs = nix::ifaddrs::getifaddrs().unwrap();
        for ifaddr in addrs {
            match ifaddr.address {
                Some(address) => match address.as_sockaddr_in() {
                    Some(addr) => {
                        let mut sa = SocketAddr::from(*addr);
                        sa.set_port(24254);
                        debug!("found local interface {:?}", sa);
                        if !self.peer_map.contains_key(&sa) {
                            self.peer_map.insert(sa, PeerInfo::new());
                        }
                        ()
                    }
                    None => (),
                },
                None => (),
            }
            match ifaddr.broadcast {
                Some(address) => match address.as_sockaddr_in() {
                    Some(addr) => {
                        let mut sa = SocketAddr::from(*addr);
                        sa.set_port(24254);
                        to_probe.insert(sa);
                        ()
                    }
                    None => (),
                },
                None => (),
            }
        }
        to_probe.insert("224.0.0.1:24254".parse().unwrap());
        for sa in to_probe.iter() {
            let message_out_bytes: Vec<u8> =
                serde_json::to_vec(&vec![Message::PleaseSendPeers(PleaseSendPeers {})]).unwrap();
            debug!( "sending message {:?} to {sa}", String::from_utf8_lossy(&message_out_bytes));
            self.socket.send_to(&message_out_bytes, sa).ok();
        }
    }
    fn probe(&mut self) -> () {
        for sa in self.best_peers(10, 3) {
            let peer_info = self.peer_map.get_mut(&sa).unwrap();
            peer_info.delay = peer_info.delay.saturating_add(peer_info.delay / 20);
            let mut message_out: Vec<Value> = Vec::new();
            message_out.push(json!(Message::PleaseSendPeers(PleaseSendPeers {})));
            // let people know im here
            // im not sure if anyone cares about all this info from completely random contacts
            message_out.push(json!(please_always_return(&self, sa)));
            message_out.push(json!(Message::MyPublicKey(MyPublicKey {
                    ed25519: self.keypair.public.clone(),
                })));
            message_out.append(&mut self.always_returned(sa));
            message_out.push(
                json!(Message::PleaseReturnThisMessage(PleaseReturnThisMessage {
                    sent_at: self.boot.elapsed().as_secs_f64(),
                })),
            );
            let message_out_bytes: Vec<u8> = serde_json::to_vec(&message_out).unwrap();

            debug!( "sending message {:?} to {sa}", String::from_utf8_lossy(&message_out_bytes));
            match self.socket.send_to(&message_out_bytes, sa) {
                Ok(s) => trace!("sent {s}"),
                Err(e) => warn!("failed to send {0} {e}", message_out_bytes.len()),
            }
        }
    }

    fn sort(&mut self) -> () {
        let mut peers: Vec<_> = self
            .peer_map
            .iter()
            .map(|(k, v)| (k, v.delay.as_secs_f64()))
            .collect::<Vec<_>>();
        peers.sort_unstable_by(|a, b| a.1.total_cmp(&b.1));
        self.peer_vec = peers.into_iter().map(|(addr, _)| *addr).collect();
    }

    fn load_peers(&mut self) -> () {
        let file = OpenOptions::new().read(true).open("peers.v3.json");
        if file.as_ref().is_ok() && file.as_ref().unwrap().metadata().unwrap().len() > 0 {
            let json: Vec<(SocketAddr, PeerInfo)> =
                serde_json::from_reader(&file.unwrap()).unwrap();
            let before = self.peer_map.len();
            self.peer_map.extend(json);
            info!("loaded {0} peers", self.peer_map.len() - before);
        }
    }
    fn save_peers(&self) -> () {
        debug!("saving peers");
        // not really sure how many, if any, of these peers or fields should be saved, or just a list of host:ips, but for the few users (1) of this so far, might as well save it all
        let mut peers_to_save: Vec<(SocketAddr, PeerInfo)> = Vec::new();
        for i in 0..cmp::min(self.peer_vec.len(), 99) {
            peers_to_save.push((self.peer_vec[i], self.peer_map[&self.peer_vec[i]].clone()))
        }

        OpenOptions::new()
            .create(true)
            .write(true)
            .truncate(true)
            .open("peers.v3.json")
            .unwrap()
            .write_all(&serde_json::to_vec_pretty(&peers_to_save).unwrap())
            .ok();
    }

    fn best_peers(&self, how_many: i32, quality: i32) -> HashSet<SocketAddr> {
        let mut rng = rand::thread_rng();
        let result: &mut HashSet<SocketAddr> = &mut HashSet::new();
        for _ in 0..how_many {
            let i = ((rng.gen_range(0.0..1.0) as f64).powi(quality) * (self.peer_vec.len() as f64))
                as usize;
            if i >= self.peer_vec.len() {
                continue;
            }
            let p = &self.peer_vec[i];
            result.insert(*p);
            trace!( "best peer(q:{quality}) {0} {1} {2}", i, p, self.peer_map[p].delay.as_secs_f64());
        }
        result.clone()
    }
    fn handle_messages(
        &mut self,
        messages: Vec<Value>,
        src: SocketAddr,
        might_be_ip_spoofing: bool,
        inbound_states: &mut HashMap<String, InboundState>,
    ) -> Vec<Value> {
        let mut message_out = vec![];
        for message_in in messages {
            if let Some(v) = message_in.get("PleaseAlwaysReturnThisMessage") {
                self.save_key(src, v.clone());
                continue;
            }
            if let Some(v) = message_in.get("PleaseReturnThisMessage") {
                message_out.push(serde_json::json!({"ReturnedMessage":v}));
                continue;
            }
            let message_in_enum: Message = match serde_json::from_value(message_in) {
                Ok(r) => r,
                Err(e) => {
                    warn!("unsupported message {e}");
                    continue;
                }
            };
            for m in match message_in_enum {
                Message::PleaseSendPeers(t) => t.send_peers(&self, might_be_ip_spoofing, src),
                Message::Peers(t) => t.receive_peers(self),
                Message::PleaseSendContent(t) =>
                    t.send_content(inbound_states, src, might_be_ip_spoofing, self),
                Message::Content(t) => t.receive_content(inbound_states, src, self),
                Message::ReturnedMessage(t) => t.update_round_trip_time(self, src),
                Message::MaybeTheyHaveSome(t) =>
                    t.add_content_peer_suggestions(self, inbound_states),
                Message::AlwaysReturned(_) => vec![], // handled before htis loop
                Message::MyPublicKey(t) => t.save_public_key(self, src),
                _ => {
                    warn!("unknown message type ");
                    vec![]
                }
            } {
                message_out.push(serde_json::json!(m));
            }
        }
        return message_out;
    }
}
fn main() -> Result<(), std::io::Error> {
    env_logger::builder()
        .format_timestamp(Some(TimestampPrecision::Millis))
        .init();
    println!("logging level: {}", log::max_level());
    let mut ps: PeerState = PeerState::new();
    // ugly    println!("your public ed25519 token for this session is:  {:?}",ps.keypair.public);

    ps.socket.set_broadcast(true).ok();
    for p in ["148.71.89.128:24254", "159.69.54.127:24254"] {
        ps.peer_map.insert(p.parse().unwrap(), PeerInfo::new());
    }
    fs::create_dir("./shared").ok();
    std::env::set_current_dir("./shared").unwrap();
    fs::create_dir("./metadata").ok();
    ps.load_peers();
    let mut args = env::args();
    args.next();
    let mut inbound_states: HashMap<String, InboundState> = HashMap::new();
    for v in args {
        info!("queing inbound file {:?}", v);
        inbound_states.insert(v.to_string(), InboundState::new(&v));
    }
    ps.socket.set_read_timeout(Some(Duration::from_secs(1)))?;
    let mut next_maintenance = Instant::now();
    let mut buf = [0; 0x10000];
    loop {
        if next_maintenance.elapsed() > Duration::ZERO {
            maintenance(&mut inbound_states, &mut ps);
            next_maintenance =
                Instant::now() + Duration::from_millis(rand::thread_rng().gen_range(888..1111));
        }
        let (message_in_len, src) = match ps.socket.recv_from(&mut buf) {
            Ok(r) => r,
            Err(_) => continue,
        };
        let message_in_bytes = &buf[0..message_in_len];
        trace!( "incoming message {:?} from {src}", String::from_utf8_lossy(message_in_bytes));
        let messages: Vec<Value> = match serde_json::from_slice(message_in_bytes) {
            Ok(r) => r,
            Err(e) => {
                warn!( "could not deserialize an incoming message {e}  :  {}",
                    String::from_utf8_lossy(message_in_bytes));
                continue;
            }
        };
        debug!("received messages {:?} from {src}", messages.len());
        if !ps.peer_map.contains_key(&src) {
            ps.peer_map.insert(src, PeerInfo::new());
            warn!("new peer spotted {src}");
        };
        let might_be_ip_spoofing = ps.check_key(&messages, src);
        // This isn't a Vec<Message> because I don't know the structure of the Please*Returns
        let mut message_out =
            ps.handle_messages(messages, src, might_be_ip_spoofing, &mut inbound_states);
        if message_out.len() == 0 {
            continue;
        }
        message_out.append(&mut ps.always_returned(src));
        if might_be_ip_spoofing {
            trim_reply(&mut message_out, message_in_len);
        }
        if message_out.len() == 0 {
            continue;
        }
        let message_out_bytes = serde_json::to_vec(&message_out).unwrap();
        trace!( "sending message {1:?} to {0}{src}", if might_be_ip_spoofing {
               "\x1b[7munverified\x1b[m "} else {""},  String::from_utf8_lossy(&message_out_bytes));
        match ps.socket.send_to(&message_out_bytes, src) {
            Ok(s) => trace!("sent {s}"),
            Err(e) => warn!("failed to send {0} {e}", message_out_bytes.len()),
        }
    }
}
fn trim_reply(message_out: &mut Vec<Value>, message_in_length: usize) {
    let mut ratio;
    let mut message_out_bytes;
    while {
        message_out_bytes = serde_json::to_vec(&message_out).unwrap();
        ratio = // 20 is IP header, 8 is UDP header
            (message_out_bytes.len() as f64 + 20.0 + 8.0) / (message_in_length  as f64 + 20.0 + 8.0);
        trace!("ratio: {ratio}");
        message_out.len() > 0 && ratio > 2.5
    } {
        debug!("{ratio}x ratio: dropping part of response to unverified source IP, so that you are not used as a flood/stressor/DDOS. {:?}", String::from_utf8_lossy(&message_out_bytes));
        message_out.pop();
        if message_out.len() == 0 {
            warn!("ratio: and none left!");
        }
    }
}

